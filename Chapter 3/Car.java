

public class Car
{
    private int MPG;
    private boolean isInStock;
    
    public Cars(int MPG, boolean isInStock)
    {
        this.MPG = MPG;
        this.isInStock = isInStock;
        
    }
    
    public getIsInStock()
    {
        return isInStock;
    }
    
     public getMPG()
    {
        return MPG;
    }
}
