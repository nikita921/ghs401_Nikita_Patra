import java.util.ArrayList;

public class Relative
{
   private int age;
   private String name;
   
   public Relative(int age, String name)
   {
       this.age = age;
       this.name = name;
   }
   
   public int getAge()
   {
       return age;
   }
   
   public String getName()
   {
       return name;
   }
}
